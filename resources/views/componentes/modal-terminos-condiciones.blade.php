                <h4>Recopilación de datos</h4>
                <p>Información registrada en el contrato con nuestra empresa, asi como la información adicional para cuestiones de autenticación, administración y transacciones bancarias digitales a traves de la web es requerida para el uso adecuado de nuestro servicio web.</p>

                <p>Informacion adicional requerida para su almacenamiento:</p>
                <ul class='ml-0'>
                    <li>Nombre personal o compañia</li>
                    <li>Correo electrónico</li>
                    <li>Cuenta de usuario</li>
                    <li>Información parcial de tarjetas bancarias para pagos digitales (<i>Requerida por la red bancaria</i>)</li>
                </ul>

                <p>Nuestros servidores <b>NO ALMACENAN</b> datos sensibles y confidenciales, como:</p>
                <ul class='ml-0'>
                    <li>Códigos de seguridad de tarjetas bancarias (CVV)</li>
                    <li>Códigos de autorización de tarjetas bancarias (NIP)</li>
                    <li>Códigos de autenticacion generados por el sistema bancario</li>
                    <li>Contraseñas de correos electrónicos</li>
                    <li>Identificación del banco emisor</li>
                </ul>
                <br>

                <h4 class='mb-3'>Uso de información</h4>

                <h5>Registro</h5>
                <p>Para el registro de usuario en nuestro servicio web, se requiere ingresar informacion de contrato como el número y el nombre de la cuenta como se extendio en el contrato. Tambien un nombre personalizado, nombre usuario, contraseña y correo electrónico.</p>
                <br>

                <h5>Autenticación</h5>
                <p>Se requiere el nombre de usuario y contraseña que registro en nuestro servicio web.
                En caso de no recordar usuario o contraseña, se requerirá número y nombre de la cuenta enlazado con el contrato y registro de usuario, como el correo electrónico donde se enviará las instrucciones de recuperación, posteriormente iniciar sesión con las indicaciones mencionadas.</p>
                <br>

                <h5>Administración</h5>
                <p>Existen opciones  de modificación a la información de autenticacion, así como la administración de cuentas y tarjetas bancarias.</p>
                <p>Para el manejo de cuentas adicionales a la principal(registro) se requiere información que menciona en su formulario correspondiente.</p>
                <p>En el tema de tarjetas bancarias, la información de estas es requerida por el sistema bancario de México para la realización de transacciones digitales. Por el cual, la información almacenada en modo persistente, solamente se usa para simplificar el proceso y agilizar la transacción digital del pago.</p>
                <br>

                <h5>Transacción</h5>
                <p>Se solicita la tarjeta bancaria almacenada y el código de seguridad  de este en modo temporal(no almacenada) para complementar la información requerida por el sistema bancario para el pago de la cuenta seleccionada.</p>
                <br>

                <h5>Eliminación</h5>
                <p>Nuestro servicio dispone opciones para dar de baja cuentas adicionales, así como las tarjetas bancarias.</p>
                <p>En caso de requerir la eliminación completa del registro de usuario, es necesario llamar al departamento de soporte técnico para evaluar y llevar acabo la acción de baja permanente.</p>
                <br>

                <h4 class='mb-3'>Información compartida a terceros</h4>
                <p>Cada transacción digital requiere la participación de servidores privados por el sistema bancario para la validación y proceso del pago digital.</p>
                <p>Para la recuperación de registro de usuario, participa el proveedor de correos electrónicos de su preferencia que registro, para el envio de instrucciones para recuperar su autenticación de usuario.</p>
                <br>

                <h4 class='mb-3'>Responsabilidades de la empresa</h4>
                <p>La confidencialidad y privacidad de la información estan bajo nuestros sistemas de seguridad de encriptación y los requerimientos de los sistemas de gestión para brindar el servicio de consulta, generación de saldos con sus respectivos vencimientos y crear el medio intermediario para realizar pagos a través de la web. Para esto, disponemos de certificados SSL/TLS para la encriptación de información entre servidores.</p>
                <p>Ciertos datos son requeridos para la identificación y control de usuario, así como otra información es requerida, gestionada y asegurada por los sistemas digitales de la red bancaria.</p>
                <p>Por lo cual nuestros servicios digitales no participan en las transacciones de estas, simplemente nuestros sistemas se convierten en intermediarios entre el usuario(tarjethabiente) y el banco relacionado a su tarjeta bancaria para ejecutar la transacción indicada.</p>
                <br>

                <h4 class='mb-3'>Responsabilidades del usuario</h4>
                <p>El ingreso de información debe ser correcta, completa a la solicitud del formulario y confidencial como la contraseña de autenticación, así como la información y seguridades de las tarjetas bancarias. El nivel de complejidad de información ingresada tales como contraseñas, nombres de usuario, etcétera, está en completa responsabilidad del usuario.</p>
                <p>Nuestro servicio web se limita en las autorizaciones y transacciones digitales del sistema bancario, como también, en los proveedores de servicio de correos electrónicos registrados para el tema de autenticación de ellos y la privacidad de la información enviada de nuestro servicio a ellos.</p>
                <p>Es importante conocer los servicios físicos como digitales de su tarjeta bancaria, para colaborar los movimientos y soporte de estos. Tambien de los servicios del proveedor de correos electrónicos para la autenticación de su correo electrónico registrado.</p>
